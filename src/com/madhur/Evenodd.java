//Write a program to check whether a number is even or odd

package com.madhur;

public class Evenodd {
    public static void main(String[] args) {
        int num = 753;
        if (num % 2 == 0) {
            System.out.println("The number " + num + " is Even");
        }
        else {
            System.out.println("The Number " + num + " is Odd");
        }
    }
}
